export default async function handler(req, res) {
  // Проверка метода запроса
  if (req.method === 'POST') {
    // Получение данных из тела запроса
    const { variant, report } = req.body;
    // Проверка валидности данных
    if (!variant || !report) {
      // Отправка ошибки
      res.status(400).json({ error: 'Недостаточно данных' });
      return;
    }
    // Сохранение данных в базе данных или файле
    // Здесь вы можете использовать любой способ хранения данных, который вам подходит
    // Например, вы можете использовать fs.writeFile для записи данных в файл
    // Или вы можете использовать mongoose для работы с MongoDB
    // Или вы можете использовать knex для работы с SQL-базами данных
    // Это зависит от вашего выбора и настроек проекта
    // Для простоты мы просто выведем данные в консоль
    console.log(`Номер варианта: ${variant}`);
    console.log(`Текст отчета: ${report}`);
    // Отправка успешного ответа
    res.status(200).json({ message: 'Отчет успешно отправлен' });
  } else {
    // Отправка ошибки для неподдерживаемого метода
    res.status(405).json({ error: 'Неподдерживаемый метод' });
  }
}
